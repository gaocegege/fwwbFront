function footerClick(id) {
    if (id == "footer_a1") {
        location.href = "index.html";
    }
    if (id == "footer_a2") {
        location.href = "mySchedule.html";
    }
    if (id == "footer_a3") {
        location.href = "nfcScan.html";
    }
}
function menuClick(id) {
    if (id == 'myInfo') {
        location.href = 'myInfo.html';
    }
    if (id == 'myMsg') {
        location.href = 'myMsg.html';
    }
    if(id=='myCar'){
        location.href = 'myCar.html';
    }
}
